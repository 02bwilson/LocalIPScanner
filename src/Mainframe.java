    public void rebuildScanTable(ArrayList<String> pings){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Port");
        model.addColumn("Status");
        int sp = Integer.parseInt(startPort.getText());
        for (int i = 0; i < pings.size(); i++){
            String[] data = {String.valueOf(sp + i), pings.get(i)};
            model.addRow(data);
        }
        portResultsTable.setDefaultEditor(Object.class, null);
        portResultsTable.setModel(model);
    }

