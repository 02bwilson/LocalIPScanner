    private void scanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scanButtonActionPerformed
        ArrayList<String> pings = new ArrayList<>();
        try {
            // Calls scan function to get ping results
            pings = scan.scanPorts(gateway.getText(), Integer.parseInt(startPort.getText()), Integer.parseInt(endPort.getText()));
            rebuildScanTable(pings);
        }
        // General Exception handler if user enters a string for end ports. 
        // By making sp/ep ints it allows for easier error checking even though they are converted to strings. 
        catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "There was a fatal error with your gateway or ports.\n" + e, "Gateway or port error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_scanButtonActionPerformed

